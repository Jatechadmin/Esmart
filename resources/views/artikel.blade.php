                                                Kemampuan jenis panel surya yang ada di pasaran saat ini, dapat menyerap dan mengubah cahaya matahari menjadi listrik rata-rata sebanyak 16-20% cahaya matahari. Artinya, tidak semua cahaya matahari dapat diubah menjadi listrik karena keterbatasan alami material sel surya yang hanya mampu menyerap radiasi cahaya matahari pada panjang gelombang (spektrum)  tertentu. Sedangkan cahaya matahari sendiri memiliki rentang panjang gelombang (spektrum) yang sangat besar.
