                                                Matahari adalah sumber energi cahaya yang dapat dimanfaatkan langsung atau dapat juga kita ubah menjadi bentuk energi lain, seperti energi panas dan energi listrik.
                                                Energi cahaya matahari dapat diubah menjadi energi panas dengan menggunakan teknologi "surya termal", alat perubahnya disebut "kolektor surya/panas" sedangkan untuk mengubah cahaya matahari menjadi listrik, digunakan teknologi "photovoltaic", nama alatnya adalah "sel surya" atau lebih dikenal dengan istilah "modul surya".
                                                Cahaya matahari ini memiliki partikel-partikel energi yang disebut "foton". Saat cahaya matahari mengenai sel surya, energi foton ini akan membangkitkan elektron-elektron yang ada dalam material sel surya tersebut sehingga menghasilkan tegangan (voltase) listrik.
                                                Itulah mengapa disebut "photovoltaic", karena berasal dari kata "photo = foton = cahaya" dan voltaic = voltase = tegangan listrik" yang artinya ; cahaya menjadi listrik.
                                                Jadi, walaupun pagi/sore, mendung atau hujan, selama masih ada cahaya matahari (tidak gelap) maka sel surya tetap akan dapat menghasilkan listrik, meski jumlahnya lebih sedikit dibandingkan saat siang terik atau kondisi cerah.

